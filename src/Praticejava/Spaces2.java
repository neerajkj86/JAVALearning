package Praticejava;

public class Spaces2 {

	public static void main(String[] args) {
		
		
		String S = "Hello India";
		
		
		
String s1 = "";


for (int i =0;i<S.length();i++)
{
	
	if (S.charAt(i)!=' ')
	{
		s1 = s1+S.charAt(i);
	}
}

System.out.println(s1);
		
		

	}

}
